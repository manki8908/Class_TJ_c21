# 크롤링(crawling)
'''
크롤링이란 웹을 탐색하는 컴퓨터 프로그램(크롤러)을 이용하여 여러 인터넷 사이트의
웹 페이지 자료를 수집해서 분류하는 과정을 말한다.
크롤러(crawler)란 자동화된 방법으로 월드와이드 웹을 탐색하는 컴퓨터 프로그램
'''

#스크래핑(scraping)
'''
스크래핑이란 웹 사이트의 내용을 긁어다 원하는 형태로 가공하는 기술을 의미한다.
즉 웹 사이트의 데이터를 수집하는 모든 작업을 의미한다. 
결국 크롤링도 스크래핑 기술의 일종이라고 할 수 있다.
'''

